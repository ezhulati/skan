import React from 'react';
import ReactDOM from 'react-dom/client';
import './index.css';
import App from './App';
import reportWebVitals from './reportWebVitals';

const root = ReactDOM.createRoot(
  document.getElementById('root') as HTMLElement
);
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);

// Register service worker for KDS offline functionality
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/sw.js')
      .then((registration) => {
        console.log('SW registered: ', registration);
        
        // Listen for updates
        registration.addEventListener('updatefound', () => {
          const newWorker = registration.installing;
          if (newWorker) {
            newWorker.addEventListener('statechange', () => {
              if (newWorker.state === 'installed' && navigator.serviceWorker.controller) {
                // New content available, prompt to refresh
                if (window.confirm('Përditësim i ri është gati. Rifresko faqen?')) {
                  window.location.reload();
                }
              }
            });
          }
        });
      })
      .catch((registrationError) => {
        console.log('SW registration failed: ', registrationError);
      });
  });
  
  // Listen for SW messages
  navigator.serviceWorker.addEventListener('message', (event) => {
    const { data } = event;
    
    switch (data.type) {
      case 'SYNC_COMPLETE':
        console.log('Background sync completed:', data.action);
        // Could dispatch a custom event here for the app to handle
        window.dispatchEvent(new CustomEvent('sw-sync-complete', { detail: data }));
        break;
        
      default:
        console.log('SW message:', data);
    }
  });
}

